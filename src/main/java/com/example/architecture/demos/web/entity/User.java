package com.example.architecture.demos.web.entity;


public class User {

  private String userName;
  private String userPasswd;
  private String userOccup;
  private String userSub;
  private String userBookmark;
  private String userEmail;
  private String userCity;
  private String userProvince;


  public String getUserName() {
    return userName;
  }

  public void setUserName(String userName) {
    this.userName = userName;
  }


  public String getUserPasswd() {
    return userPasswd;
  }

  public void setUserPasswd(String userPasswd) {
    this.userPasswd = userPasswd;
  }


  public String getUserOccup() {
    return userOccup;
  }

  public void setUserOccup(String userOccup) {
    this.userOccup = userOccup;
  }


  public String getUserSub() {
    return userSub;
  }

  public void setUserSub(String userSub) {
    this.userSub = userSub;
  }


  public String getUserBookmark() {
    return userBookmark;
  }

  public void setUserBookmark(String userBookmark) {
    this.userBookmark = userBookmark;
  }


  public String getUserEmail() {
    return userEmail;
  }

  public void setUserEmail(String userEmail) {
    this.userEmail = userEmail;
  }


  public String getUserCity() {
    return userCity;
  }

  public void setUserCity(String userCity) {
    this.userCity = userCity;
  }


  public String getUserProvince() {
    return userProvince;
  }

  public void setUserProvince(String userProvince) {
    this.userProvince = userProvince;
  }

}
